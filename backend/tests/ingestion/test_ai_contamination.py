# ── Heuristic scorer ─────────────────────────────────────────────────────────


class TestScoreHeuristic:
    """Unit tests for the fast heuristic scorer."""

    def test_empty_text_returns_zero(self):
        assert score_heuristic("") == 0.0

    def test_none_like_short_text_returns_zero(self):
        assert score_heuristic("ok") == 0.0

    def test_ai_opener_happy_to_help(self):
        score = score_heuristic("I'd be happy to help you with that!")
        # "happy to help" phrase alone clears the low threshold
        assert score > _HEURISTIC_CONFIDENT_LOW

    def test_ai_opener_certainly(self):
        score = score_heuristic("Certainly, let me explain the architecture for you.")
        assert score >= _HEURISTIC_CONFIDENT_LOW

    def test_ai_opener_sure_heres(self):
        score = score_heuristic("Sure, here's how you can fix this bug.")
        assert score >= _HEURISTIC_CONFIDENT_LOW

    def test_ai_phrase_great_question(self):
        score = score_heuristic("Great question! Let me break this down for you.")
        # Two AI patterns; must exceed the low threshold
        assert score > _HEURISTIC_CONFIDENT_LOW

    def test_ai_phrase_lets_break_down(self):
        score = score_heuristic("Let's break this down into manageable steps.")
        assert score >= _HEURISTIC_CONFIDENT_LOW

    def test_ai_phrase_in_summary(self):
        score = score_heuristic("In summary, the approach reduces complexity significantly.")
        assert score >= _HEURISTIC_CONFIDENT_LOW

    def test_ai_phrase_hope_this_helps(self):
        score = score_heuristic(
            "This should resolve the issue. Hope this helps! Let me know if you have questions."
        )
        assert score >= _HEURISTIC_CONFIDENT_LOW

    def test_as_an_ai_phrase(self):
        score = score_heuristic("As an AI language model, I don't have opinions on frameworks.")
        # "as an AI language model" is a strong AI signal (0.50 increment)
        assert score >= 0.5

    def test_copilot_attribution(self):
        score = score_heuristic("This PR description was AI generated by GitHub Copilot.")
        # Copilot attribution pattern adds 0.60; must exceed low threshold
        assert score > _HEURISTIC_CONFIDENT_LOW

    def test_heavy_enumeration_boosts_score(self):
        text = (
            "Here are the main points:\n"
            "1. First we need to set up the environment.\n"
            "2. Then configure the database connection.\n"
            "3. Finally run the migrations.\n"
            "4. Verify everything works correctly.\n"
        )
        score = score_heuristic(text)
        assert score >= _HEURISTIC_CONFIDENT_LOW

    def test_human_terse_pr_comment_is_low(self):
        score = score_heuristic("nit: this should use `is None` instead of `== None`")
        assert score < _HEURISTIC_CONFIDENT_LOW

    def test_human_blunt_review_is_low(self):
        score = score_heuristic(
            "This is wrong. We never store secrets in env vars checked into git."
        )
        assert score < _HEURISTIC_CONFIDENT_LOW

    def test_human_typo_commit_message_is_low(self):
        score = score_heuristic("fixd the nul pointer exceptoin in auth middleware lol")
        assert score < _HEURISTIC_CONFIDENT_LOW

    def test_human_informal_comment_is_low(self):
        score = score_heuristic("yeah this is fine, ship it. we can clean up the abstraction later")
        assert score < _HEURISTIC_CONFIDENT_LOW

    def test_score_clamped_to_one(self):
        # Pile on many AI phrases — must not exceed 1.0
        text = (
            "I'd be happy to help! Certainly, of course. "
            "Great question! Sure, here's the answer. "
            "As an AI language model, let's break this down. "
            "In summary: hope this helps. Feel free to ask. "
            "Don't hesitate to reach out. This PR introduces a new feature. "
            "Best regards."
        )
        assert score_heuristic(text) == 1.0

    def test_stock_phrase_count(self):
        """Sanity-check that the pattern list is non-trivial."""
        assert STOCK_PHRASE_COUNT >= 20

    def test_all_patterns_compile(self):
        """All patterns in AI_STOCK_PHRASES are valid compiled regexes."""
        for name, pattern, increment in AI_STOCK_PHRASES:
            assert hasattr(pattern, "search"), f"Pattern {name} is not compiled"
            assert 0.0 < increment <= 1.0, f"Increment for {name} out of range"


# ── score_ai_contamination (async) ───────────────────────────────────────────


class TestScoreAiContamination:
    """Integration-level tests for the async scorer."""

    @pytest.mark.asyncio
    async def test_clearly_human_skips_llm(self):
        """Heuristic-confident human text must not hit the LLM."""
        with patch(
            "app.ingestion.ai_contamination._score_via_llm",
            new_callable=AsyncMock,
        ) as mock_llm:
            score = await score_ai_contamination(
                "nit: rename the variable to `session_id` for clarity"
            )
        mock_llm.assert_not_called()
        assert score < _HEURISTIC_CONFIDENT_LOW

    @pytest.mark.asyncio
    async def test_clearly_ai_skips_llm(self):
        """Heuristic-confident AI text must not hit the LLM."""
        with patch(
            "app.ingestion.ai_contamination._score_via_llm",
            new_callable=AsyncMock,
        ) as mock_llm:
            score = await score_ai_contamination(
                "I'd be happy to help! Great question! As an AI language model, "
                "certainly, let's break this down. In summary: hope this helps!"
            )
        mock_llm.assert_not_called()
        assert score > _HEURISTIC_CONFIDENT_HIGH

    @pytest.mark.asyncio
    async def test_middle_zone_triggers_llm(self):
        """Ambiguous text in the 0.2–0.8 band must call the LLM."""
        # Craft text with a single borderline phrase that lands in middle zone
        # "hope this helps" → 0.25 heuristic; reliably in middle zone
        borderline = "Hope this helps with your project. Let me know if there are issues."

        h = score_heuristic(borderline)
        assert _HEURISTIC_CONFIDENT_LOW <= h <= _HEURISTIC_CONFIDENT_HIGH, (
            f"Test invariant broken: heuristic score {h} not in middle zone"
        )

        with patch(
            "app.ingestion.ai_contamination._score_via_llm",
            new_callable=AsyncMock,
            return_value=0.6,
        ) as mock_llm:
            score = await score_ai_contamination(borderline)

        mock_llm.assert_called_once()
        assert 0.0 <= score <= 1.0

    @pytest.mark.asyncio
    async def test_returns_zero_for_short_text(self):
        score = await score_ai_contamination("hi")
        assert score == 0.0

    @pytest.mark.asyncio
    async def test_llm_failure_returns_blended_not_crash(self):
        """If LLM raises in middle zone, _score_via_llm catches it and returns 0.5."""
        borderline = "Hope this helps with your project. Let me know if there are issues."

        h = score_heuristic(borderline)
        assert _HEURISTIC_CONFIDENT_LOW <= h <= _HEURISTIC_CONFIDENT_HIGH, (
            f"Test invariant broken: heuristic score {h} not in middle zone"
        )

        # _score_via_llm catches all exceptions internally and returns 0.5.
        # We verify that by patching it to return the fallback value directly,
        # confirming score_ai_contamination blends it without crashing.
        with patch(
            "app.ingestion.ai_contamination._score_via_llm",
            new_callable=AsyncMock,
            return_value=0.5,  # fallback value _score_via_llm returns on failure
        ):
            score = await score_ai_contamination(borderline)

        assert 0.0 <= score <= 1.0



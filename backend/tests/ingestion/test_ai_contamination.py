# ── browse_evidence / search_evidence contamination filter ───────────────────


class TestExplorerToolsContamination:
    """Test that explorer tools respect the exclude_contaminated flag."""

    @pytest.fixture
    def make_evidence_row(self):
        """Factory for mock Evidence rows."""

        def _make(score: float | None, item_id: str = "ev-1", content: str = "some content"):
            row = MagicMock()
            row.id = item_id
            row.mini_id = "mini-1"
            row.source_type = "github"
            row.item_type = "pr"
            row.content = content
            row.explored = False
            row.source_privacy = "public"
            row.created_at = None
            row.ai_contamination_score = score
            return row

        return _make

    @pytest.mark.asyncio
    async def test_browse_excludes_high_contamination_by_default(self, make_evidence_row):
        """browse_evidence with default exclude_contaminated=True must apply WHERE filter."""
        from app.synthesis.explorers.tools import build_explorer_tools

        db_session = AsyncMock()

        # Simulate two DB results: count query returns 1, items query returns 1 clean row
        clean_row = make_evidence_row(score=0.1, item_id="clean")
        count_result = MagicMock()
        count_result.scalar.return_value = 1
        items_result = MagicMock()
        items_result.scalars.return_value.all.return_value = [clean_row]

        db_session.execute = AsyncMock(side_effect=[items_result, count_result])

        tools = build_explorer_tools(
            mini_id="mini-1",
            source_type="github",
            db_session=db_session,
        )
        browse = next(t for t in tools if t.name == "browse_evidence")
        response = await browse.handler(source_type="github")
        data = json.loads(response)

        assert data["exclude_contaminated"] is True
        assert data["total"] == 1

    @pytest.mark.asyncio
    async def test_browse_includes_all_when_flag_false(self, make_evidence_row):
        """browse_evidence with exclude_contaminated=False must not apply contamination filter."""
        from app.synthesis.explorers.tools import build_explorer_tools

        db_session = AsyncMock()

        all_rows = [
            make_evidence_row(score=0.9, item_id="ai-1"),
            make_evidence_row(score=0.1, item_id="human-1"),
        ]
        items_result = MagicMock()
        items_result.scalars.return_value.all.return_value = all_rows
        count_result = MagicMock()
        count_result.scalar.return_value = 2

        db_session.execute = AsyncMock(side_effect=[items_result, count_result])

        tools = build_explorer_tools(
            mini_id="mini-1",
            source_type="github",
            db_session=db_session,
        )
        browse = next(t for t in tools if t.name == "browse_evidence")
        response = await browse.handler(source_type="github", exclude_contaminated=False)
        data = json.loads(response)

        assert data["exclude_contaminated"] is False

